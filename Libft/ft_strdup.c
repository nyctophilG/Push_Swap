#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s;
	size_t	i;

	s = (char *)malloc(ft_strlen(s1) + 1);
	if (s == NULL)
	{
		return (NULL);
	}
	i = 0;
	while (s1[i] != '\0')
	{
		s[i] = s1[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
